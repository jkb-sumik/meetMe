const MapScreen = (props) => {
  const userData = useSelector((state) => state.auth.userData);
  const selectedUser = useSelector((state) => state.users.storedUsers);
  const dataaa = [...Object.values(selectedUser), userData];

  return (
    <View style={styles.container}>
      <MapView
        initialRegion={{
          latitude: userData.coords.latitude,
          longitude: userData.coords.longitude,
          latitudeDelta: 0.0922,
          longitudeDelta: 0.0421,
        }}
        style={styles.map}
        customMapStyle={MAP_STYLE}
      >
        {dataaa.map((marker, index) => (
          <Marker
            key={index}
            coordinate={{
              latitude: marker.coords.latitude,
              longitude: marker.coords.longitude,
            }}
            title={marker.firstLast}
            description={marker.about}
          >
            <MyCustomMarkerView image={marker.profilePicture} />
          </Marker>
        ))}
      </MapView>
    </View>
  );
};

const styles = StyleSheet.create({
  container: {
    flex: 1,
  },
  map: {
    width: "100%",
    height: "100%",
  },
});

export default MapScreen;
